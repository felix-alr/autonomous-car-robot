package com.example.robothmi3;

public class ParkingSpot {
    public float id, x1, y1, x2, y2, suitable;

    public ParkingSpot(float id, float x1, float y1, float x2, float y2, float suitable) {
        this.id = id;
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.suitable = suitable;
    }
}
